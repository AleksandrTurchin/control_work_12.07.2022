# 3) Пользователь вводит строку. В строке заменить пробелы звездочкой. Если встречается подряд несколько пробелов,
# то их следует заменить одним знаком "*", пробелы в начале и конце строки удалить. Результаты вывести на экран.

_str = input('Input string: ')
del_space = _str.strip()  # удаляем пробелы в начале и в конце строки
list_strings = del_space.split()  # делим строку на подстроки
new_str = '*'.join(list_strings)  # собираем в строку с пробелами ввиде *
print(new_str)


