# 1) Квадратное уравнение имеет вид ax^2 + bx + c = 0. Пользователь вводит a, b и c. Необходимо вычислить значение x.
# Результаты вывести на экран.
# Математические подсказки:
# сначала вычисляют дискриминант по формуле D = b2 - 4ac
# Если D > 0, то  x1 = (-b + D1/2 ) / (2a)  и x2 = (-b - D1/2 ) / (2a)
# Если D = 0, то x = -b  / (2a)
# Если D < 0, то корней нет.

a = float(input('Input a: '))
b = float(input('Input a: '))
c = float(input('Input a: '))
D = b ** 2 - 4 * a * c
if D > 0:
    x1 = (- b + D ** 0.5) / (2 * a)
    x2 = (- b - D ** 0.5) / (2 * a)
    print(x1, x2)
elif D == 0:
    x = (- b) / (2 * a)
    print(x)
else:
    print('No')


